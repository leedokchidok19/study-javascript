// 할당연산자 (Assignment operators)
let a = 1;
a = a + 2;

console.log(a); // 3

a += 2; // a = a + 2; 축약버전

console.log(a); // 5

a -= 2; // a = a - 2; 축약버전

console.log(a); // 3

a *= 2; // a = a * 2; 축약버전

console.log(a); // 6

a /= 2; // a = a / 2; 축약버전

console.log(a); // 3

a %= 2; // a = a % 2; 축약버전

console.log(a); // 1

a **= 2; // a = a ** 2; 축약버전

console.log(a); // 1